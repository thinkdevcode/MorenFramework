#ifndef MOREN_GUIOBJECT_HPP
#define MOREN_GUIOBJECT_HPP

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include "../Kernel.hpp"

namespace Moren
{

////////////////////////////////////////////////////////////
/// GUIObjects are customizable objects that can be added to
/// the GUI container, Some Pre-Built types are available.
/// This is a pure virtual class, do not initialize directly
///
////////////////////////////////////////////////////////////
class GUIObject
{
public:

	////////////////////////////////////////////////////////////
	/// /see Drawable::Render
	///
	////////////////////////////////////////////////////////////
	virtual void Render(RenderTarget& Target, RenderQueue& queue) =0;

	////////////////////////////////////////////////////////////
	/// Perform any updating of the object in this function
	/// (Can be used for animated/moving objects, etc)
	///
	////////////////////////////////////////////////////////////
	virtual void Update() =0;

	////////////////////////////////////////////////////////////
	/// Process Events (such as On Click, Mouse Over, etc.)
	///
	////////////////////////////////////////////////////////////
	virtual void ProcessEvent(Event& EventPacket) =0;

	////////////////////////////////////////////////////////////
	// GUI Events
	//
	////////////////////////////////////////////////////////////
	virtual void OnRightClick() =0;
	virtual void OnLeftClick() =0;
	virtual void OnMouseOver() =0;
	virtual void OnMouseOff() =0;
};

} // namespace Moren

#endif // MOREN_GUIOBJECT_HPP